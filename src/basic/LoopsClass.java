package basic;

public class LoopsClass {

	public static void main(String[] args) {
		//1. While Loop // If we comment the / missed Increment Part it will print infinite. This is the disadvantage
		//2. For loop
		
		int a = 2;   // initilization
		while (a<=20) {  // Condition
			System.out.println(a);
			a = a + 2;   // Increment Part
		}

	}

}
